    /// <summary>
    /// Execute a one-shot overlap query at this hitbox's current world position and size.
    /// Call this at the exact peak frame of an attack from PawnCombatBehaviour / EnemyAI.
    /// Clears the per-swing hit set, so each Fire call is a fresh swing.
    /// </summary>
    public void Fire(float damage, float knockback)
    {
        _hitThisSwing.Clear();
#if UNITY_EDITOR
        _gizmoArmedUntil = UnityEngine.Time.realtimeSinceStartup + GizmoFlashDuration;
#endif

        int count;
        if (_col is BoxCollider box)
        {
            Vector3 worldCenter = transform.TransformPoint(box.center);
            Vector3 halfExtents = Vector3.Scale(box.size * 0.5f, transform.lossyScale);
            count = Physics.OverlapBoxNonAlloc(
                worldCenter, halfExtents, _overlapBuffer,
                transform.rotation,
                Physics.DefaultRaycastLayers,
                QueryTriggerInteraction.Ignore);   // ÃƒÂ¢Ã¢â‚¬Â Ã‚Â skips all other HitBox triggers
        }
        else if (_col is SphereCollider sphere)
        {
            Vector3 worldCenter = transform.TransformPoint(sphere.center);
            float   worldRadius = sphere.radius * Mathf.Max(
                                      transform.lossyScale.x,
                                      transform.lossyScale.y,
                                      transform.lossyScale.z);
            count = Physics.OverlapSphereNonAlloc(
                worldCenter, worldRadius, _overlapBuffer,
                Physics.DefaultRaycastLayers,
                QueryTriggerInteraction.Ignore);
        }
        else
        {
            return; // unsupported collider type
        }

        for (int i = 0; i < count; i++)
            ProcessHit(_overlapBuffer[i], damage, knockback);
    }

