    private void ProcessHit(Collider other, float damage, float knockback)
    {
        var health = other.GetComponentInParent<HealthComponent>();
        if (health == null)                            return;
        if (health.gameObject == owner)                return;
        if (_hitThisSwing.Contains(health.gameObject)) return;
        _hitThisSwing.Add(health.gameObject);

