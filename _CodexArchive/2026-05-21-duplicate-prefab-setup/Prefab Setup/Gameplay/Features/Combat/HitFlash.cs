    [Tooltip("Color to display on hit. White gives a clean 'bright impact' look.")]
    [SerializeField] private Color flashColor = Color.white;

    [Tooltip("How long the flash lasts in real seconds. " +
             "Keep this at or slightly above the freeze frame duration so they overlap cleanly.")]
    [Range(0.01f, 0.5f)]
    [SerializeField] private float flashDuration = 0.07f;

