/// <summary>
/// Flashes the SpriteRenderer to a configurable color whenever the sibling
/// HealthComponent receives damage. Uses WaitForSecondsRealtime so the flash
/// fires and resolves correctly even during a TimeManager freeze-frame.
///
/// Setup:
///   1. Attach to the same GameObject as the SpriteRenderer and HealthComponent.
///   2. Tune Flash Color (default white) and Flash Duration (default 0.07 s).
///      The SpriteRenderer's normal color is restored automatically after the flash.
/// </summary>
