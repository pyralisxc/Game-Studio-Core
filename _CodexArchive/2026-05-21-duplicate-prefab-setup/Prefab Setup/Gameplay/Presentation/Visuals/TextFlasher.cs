    private void Initialize()
    {
        if (_initialized) return;
        _initialized = true;

        if (_autoFindTexts && _texts.Count == 0)
            _texts.AddRange(GetComponentsInChildren<TMP_Text>(true));

        // Reset _FaceColor to opaque white on the shared material (not fontMaterial) so
        // we don't create a per-instance material copy. fontMaterial triggers an instance;
        // sharedMaterial writes back to the atlas asset which is safe for a global reset.
        // This guards against a stale alpha=0 left by a previous Play Mode session.
        foreach (var t in _texts)
            if (t != null) t.fontSharedMaterial.SetColor(FaceColorId, Color.white);

        CacheOriginalColors();
    }

    private void CacheOriginalColors()
    {
        _originalColors = new Color[_texts.Count];
        for (int i = 0; i < _texts.Count; i++)
            _originalColors[i] = _texts[i] != null ? _texts[i].color : Color.white;
    }

