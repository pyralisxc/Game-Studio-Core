    private Color[] CaptureBaseColors(FlashPresetSO preset)
    {
        var colors = new Color[_texts.Count];
        for (int i = 0; i < _texts.Count; i++)
        {
            if (_texts[i] == null) { colors[i] = Color.white; continue; }
            colors[i] = preset.useRendererColorAsBase ? _texts[i].color : preset.baseColor;
            if (preset.overrideAlpha) colors[i].a = preset.baseAlpha;
        }
        return colors;
    }

    private void SetAll(Color color)
    {
        foreach (var t in _texts)
            if (t != null) t.color = color;
    }

    private void RestoreToCapture(Color[] baseColors)
    {
        for (int i = 0; i < _texts.Count && i < baseColors.Length; i++)
            if (_texts[i] != null) _texts[i].color = baseColors[i];
    }

    private void RestoreOriginalColors()
    {
        if (_originalColors == null) return;
        for (int i = 0; i < _texts.Count && i < _originalColors.Length; i++)
            if (_texts[i] != null) _texts[i].color = _originalColors[i];
    }

    private static Color WithAlpha(Color color, bool applyOverride, float overrideAlpha, Color[] baseColors)
    {
        color.a = applyOverride ? overrideAlpha : (baseColors.Length > 0 ? baseColors[0].a : 1f);
        return color;
    }

    private static float ApplyEase(FlashPresetSO.FlashEase ease, float t)
    {
        switch (ease)
        {
            case FlashPresetSO.FlashEase.Linear:    return t;
            case FlashPresetSO.FlashEase.InSine:    return 1f - Mathf.Cos(t * Mathf.PI * 0.5f);
            case FlashPresetSO.FlashEase.OutSine:   return Mathf.Sin(t * Mathf.PI * 0.5f);
            case FlashPresetSO.FlashEase.InOutSine: return -(Mathf.Cos(Mathf.PI * t) - 1f) * 0.5f;
            case FlashPresetSO.FlashEase.InQuad:    return t * t;
            case FlashPresetSO.FlashEase.OutQuad:   return 1f - (1f - t) * (1f - t);
            case FlashPresetSO.FlashEase.InOutQuad: return t < 0.5f ? 2f * t * t : 1f - Mathf.Pow(-2f * t + 2f, 2f) * 0.5f;
            case FlashPresetSO.FlashEase.InCubic:   return t * t * t;
            case FlashPresetSO.FlashEase.OutCubic:  return 1f - Mathf.Pow(1f - t, 3f);
            default:             return Mathf.SmoothStep(0f, 1f, t);
        }
    }
