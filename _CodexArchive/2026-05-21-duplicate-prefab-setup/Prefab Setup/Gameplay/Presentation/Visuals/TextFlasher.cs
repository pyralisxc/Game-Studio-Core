    private IEnumerator FlashRoutine(FlashPresetSO preset, int loops)
    {
        Color[] baseColors = CaptureBaseColors(preset);
        int     played     = 0;

        while (loops < 0 || played < loops)
        {
            switch (preset.mode)
            {
                case FlashPresetSO.FlashMode.Pulse:      yield return PulseRoutine(preset, baseColors);      break;
                case FlashPresetSO.FlashMode.Strobe:     yield return StrobeRoutine(preset, baseColors);     break;
                case FlashPresetSO.FlashMode.Blink:      yield return BlinkRoutine(preset, baseColors);      break;
                case FlashPresetSO.FlashMode.ColorCycle: yield return ColorCycleRoutine(preset, baseColors); break;
            }

            if (loops > 0) played++;

            // cycleDelay: pause between repeats (not before the first).
            // For non-ColorCycle modes the base color is shown during this gap.
            // For ColorCycle the last step color simply holds until the next cycle starts.
            bool moreLoops = loops < 0 || played < loops;
            if (moreLoops && preset.cycleDelay > 0f)
            {
                if (preset.mode != FlashPresetSO.FlashMode.ColorCycle)
                    RestoreToCapture(baseColors);
                yield return new WaitForSeconds(preset.cycleDelay);
            }
        }

        RestoreToCapture(baseColors);
        _onFlashComplete?.Invoke();
    }

