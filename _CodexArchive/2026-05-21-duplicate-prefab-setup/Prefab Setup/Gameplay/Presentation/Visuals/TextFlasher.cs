    /// <summary>Plays the given preset, stopping any active flash first.</summary>
    public void Play(FlashPresetSO preset)
    {
        if (preset == null) { Debug.LogWarning("[TextFlasher] Play called with no preset.", this); return; }
        Stop();
        if (!_initialized) Initialize();
        _routine = StartCoroutine(FlashRoutine(preset, preset.loopCount < 0 ? -1 : Mathf.Max(1, preset.loopCount)));
    }

    /// <summary>Plays the given preset exactly once regardless of its loopCount setting.</summary>
    public void PlayOneShot(FlashPresetSO preset)
    {
        if (preset == null) return;
        Stop();
        if (!_initialized) Initialize();
        _routine = StartCoroutine(FlashRoutine(preset, 1));
    }

    /// <summary>Stops the active flash immediately and restores all texts to their original colors.</summary>
    public void Stop()
    {
        if (_routine != null) { StopCoroutine(_routine); _routine = null; }
        RestoreOriginalColors();
    }

